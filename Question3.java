package as4;

public class Question3 {
//Define variable counter
	// Add 1 to variable until 10
	//Sum every value that Variable became with the last
	public static void main(String[] args) {
	
		
	//Initialize sum variable
		int sum = 0;
		//increment variable "a" as long as it is <=10 
		for(int a = 1; a <= 10; a++) {
			//Use the loop to increment variable "sum" by a's value
		sum+=a;
	
		}
		//print sum
		System.out.println("The sum of numbers 1-10 = " + sum);	
	
	}

}

	

	

